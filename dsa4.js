//1910. Remove All Occurrences of a Substring
/**
 * @param {string} s
 * @param {string} part
 * @return {string}
 */
var removeOccurrences = function(s, part) {
    
    while(s <s.includes(part)){
        
        
            s = s.split(part).join("");
            return s;
         
        
    
    }
    
};
